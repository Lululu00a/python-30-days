"""
练习 4 参考答案：可变 vs 不可变对象
"""

print("===== 代码 A：不可变对象 int =====")
a = 10
b = a
a = 20
print(f"a={a}, b={b}")
# 输出：a=20, b=10

print("\n===== 代码 B：可变对象 list =====")
lst1 = [1, 2, 3]
lst2 = lst1
lst1.append(4)
print(f"lst1={lst1}")
print(f"lst2={lst2}")
# 输出：lst1=[1, 2, 3, 4]
#       lst2=[1, 2, 3, 4]

# ==================================================
# 我的解释：
#
# 代码 A 中 b 不变，因为：
#   int 是不可变对象。
#   `b = a` 让 b 也指向对象 10；
#   `a = 20` 不是修改对象 10，而是把标签 a 撕下来，贴到新对象 20 上。
#   b 仍然指向 10，所以 b 没变。
#
# 代码 B 中 lst2 也变了，因为：
#   list 是可变对象。
#   `lst2 = lst1` 让 lst1 和 lst2 指向同一个 list 对象。
#   `lst1.append(4)` 是【原地修改】这个 list 对象（往里加元素）。
#   既然 lst2 也指向这个 list，看到的当然也是修改后的结果。
#
# 🔑 核心区别：
#   - 不可变对象：赋值/"修改"会创建新对象，老对象不受影响
#   - 可变对象：原地修改，所有指向它的标签都会"看到"变化
# ==================================================
